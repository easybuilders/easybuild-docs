"""
Generates documentation for (generic) simpleblocks in MarkDown format

@author Caroline De Brouwer (Ghent University)
@author Kenneth Hoste (Ghent University)
"""

import os
import simplebuild.tools.config as config

from simplebuild.base.generaloption import simple_option
from simplebuild.tools.docs import gen_simpleblocks_overview_md
from simplebuild.tools.filetools import write_file

COMMON_PARAMS = {
    'ConfigureMake': ['configopts', 'buildopts', 'installopts'],
    # needs to be extended
}
DOC_FUNCTIONS = ['build_step', 'configure_step', 'install_step']

DEFAULT_EXAMPLE_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'example_simpleconfigs')
DEFAULT_MODULE = 'simplebuild.simpleblocks.generic'


options = {
    'out-file': ('Path to output file', 'string', 'store', None, 'o'),
    'examples': ('Path to dir that contains example files', 'string', 'store', DEFAULT_EXAMPLE_PATH, 'e'),
    'module': ('Name of module to load the simpleblocks from', 'string', 'store', DEFAULT_MODULE, 'm')
}
so = simple_option(options)

config.init_build_options({'validate': False, 'external_modules_metadata': {}})

autogen_comment = [
    "<!---",
    "    This file is automatically generated using the %s script, " % os.path.basename(__file__),
    "    and information and docstrings from simpleblocks and the SimpleBuild framework.",
    "    Do not edit this file manually, but update the docstrings and regenerate it!",
    "-->",
    '',
]

simpleblocks_overview = gen_simpleblocks_overview_md(so.options.module, so.options.examples, COMMON_PARAMS, DOC_FUNCTIONS)

txt = '\n'.join(autogen_comment + simpleblocks_overview)
if so.options.out_file:
    write_file(so.options.out_file, txt)
    print('%s updated' % so.options.out_file)
else:
    print(txt)
